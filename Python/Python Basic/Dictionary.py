#Dictionary:
'''a = {'Brand':'Ford', 'Model':'Mustag', 'Year':1964}
print(a)'''

#Dictionary Items:
'''a = {'Brand':'Ford',
     'Model':'Mustag',
     'Year':1964
     }
print(a['Brand'])'''

#Accessing Items
'''a = {'Brand':'Ford', 'Model':'Mustag', 'Year':1964}
x = a['Model']
print(x)'''

#Change Values
'''a = {'Brand':'Ford', 'Model':'Mustag', 'Year':1964}
a['Year'] = 2018
print(a)'''

#Update Dictionary
'''a = {'Brand':'Ford', 'Model':'Mustag', 'Year':1964}
a.update({'Year':2020})
print(a)'''

#Adding items
'''a = {'Brand':'Ford', 'Model':'Mustag', 'Year':1964}
a['Color']='Red'
print(a)'''

#Removing Items:
'''a = {'Brand':'Ford', 'Model':'Mustag', 'Year':1964}
a.pop('Model')
print(a)'''

